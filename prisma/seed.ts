async function main() {
  console.log("Seeding database with Dutch venues...");

  await prisma.venue.deleteMany();
  await prisma.booking.deleteMany();

  for (const venue of venues) {
    const amenities = venue.amenities;
    await prisma.venue.create({
      data: {
        name: venue.name,
        slug: venue.slug,
        city: venue.city,
        address: venue.address,
        description: venue.description,
        shortDescription: venue.shortDescription,
        capacity: venue.capacity,
        pricePerDay: venue.pricePerDay,
        pricePerHalfDay: venue.pricePerHalfDay,
        amenities: JSON.stringify(amenities),
        images: JSON.stringify(venue.images),
        venueType: venue.venueType,
        rating: venue.rating,
        latitude: venue.latitude,
        longitude: venue.longitude,
        facilities: makeFacilities(amenities),
        roomLayouts: makeRoomLayouts(venue.capacity, venue.venueType),
        transportInfo: venue.transportInfo,
        parkingInfo: venue.parkingInfo,
        termsAndConditions: venue.termsAndConditions,
        sustainabilityInfo: venue.sustainabilityInfo,
        meetingExpertName: venue.meetingExpertName,
        meetingExpertRole: venue.meetingExpertRole,
        meetingExpertImage: venue.meetingExpertImage,
        phoneNumber: venue.phoneNumber,
        email: venue.email,
        status: "approved",
        isAiGenerated: false,
      },
    });
  }

  const count = await prisma.venue.count();
  console.log(`Seeded ${count} venues successfully.`);
}

